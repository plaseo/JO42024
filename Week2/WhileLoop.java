package Week2;
public class WhileLoop {
    public static void main(String[] args) {
        // ++ alows you to increase a value by 1
        // -- allows you to decrease a value by 1
        int x = 1;
        while (x <=10){
            System.out.println(x);
            //x = x + 1
            x++;
        }
    }
    
}
